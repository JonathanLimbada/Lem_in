#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t k;

	i = 0;
	k = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[k] != '\0' && k < n)
	{
		s1[i++] = s2[k];
		if ((k + 1) == n)
		{
			s1[i] = '\0';
			return (s1);
		}
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
