#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*p;

	i = 0;
	p = s;
	while (i < n)
	{
		((unsigned char *)p)[i] = '\0';
		i++;
	}
	s = p;
}
