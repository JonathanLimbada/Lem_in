char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (s1[i])
		i++;
	while (*s2)
	{
		s1[i + k] = s2[k];
		if (s2[k] == '\0')
			return (s1);
		k++;
	}
	return (s1);
}
